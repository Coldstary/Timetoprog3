using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace LibrarsStraightLine
{
    // Статический класс для работы с прямыми линиями (неограниченными)
    public static class StraightLine
    {
        // Метод для определения пересечения прямой линии с осями координат
        public static string Intersection_Of_AxesStraightLine(string input)
        {
            // Разделение входной строки на части по пробелам
            string[] pointParts = input.Split(' ');

            // Проверка наличия всех четырех координат (две точки: x1, y1, x2, y2)
            if (pointParts.Length == 4)
            {
                // Проверка: если абсолютные значения x-координат равны
                // Это означает, что прямая параллельна оси OY (вертикальная линия)
                if (Math.Abs(Convert.ToInt32(pointParts[0])) == Math.Abs(Convert.ToInt32(pointParts[2])))
                {
                    // Вертикальная прямая пересекает ось OY (ось Y) только в одной точке x=0
                    // ВНИМАНИЕ: На самом деле вертикальная прямая параллельна оси OY и пересекает ось OX
                    return $"Пересекает ось OY";
                }
                // Проверка: если абсолютные значения y-координат равны
                // Это означает, что прямая параллельна оси OX (горизонтальная линия)
                else if (Math.Abs(Convert.ToInt32(pointParts[1])) == Math.Abs(Convert.ToInt32(pointParts[3])))
                {
                    // Горизонтальная прямая пересекает ось OX (ось X) только в одной точке y=0
                    // ВНИМАНИЕ: На самом деле горизонтальная прямая параллельна оси OX и пересекает ось OY
                    return $"Пересекает ось OX";
                }
                else
                {
                    // Если ни x-координаты, ни y-координаты не равны по модулю
                    // Прямая не параллельна ни одной оси и пересекает обе оси
                    return $"Пересекает оси OY и OX";
                }
            }
            // Проверка на недостаточное количество данных
            else if (pointParts.Length < 4)
            {
                return "(не достаточно данных)";
            }

            // Сообщение об ошибке обработки в других случаях
            return "Ошибка в обработке";
        }
    }
}
